package com.barclays.homeLoanApplication.controller;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;

import com.barclays.homeLoanApplication.entity.EmailDetails;
//import com.barclays.homeLoanApplication.service.EmailService;
import com.barclays.homeLoanApplication.serviceImpl.EmailServiceImpl;

public class EmailController {
	@Autowired
	private EmailServiceImpl emailService;
	 
    // Sending a simple Email
    @PostMapping("/sendMail")
    public String
    sendMail(@RequestBody EmailDetails details)
    {
        String status
            = emailService.sendSimpleMail(details);
 
        return status;
    }
 
    // Sending email with attachment
    @PostMapping("/sendMailWithAttachment")
    public String sendMailWithAttachment(
        @RequestBody EmailDetails details)
    {
        String status
            = emailService.sendMailWithAttachment(details);
 
        return status;
    }
}
